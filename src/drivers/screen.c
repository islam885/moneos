void screen_init(struct multiboot_info *mbi) {
    framebuffer = (uint32_t *)(uintptr_t)mbi->framebuffer_addr;
    width = mbi->framebuffer_width;
    height = mbi->framebuffer_height;
    pitch = mbi->framebuffer_pitch / 4;
}

void screen_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x < width && y < height) {
        framebuffer[y * pitch + x] = color;
    }
}

void screen_clear(uint32_t color) {
    for (uint32_t y = 0; y < height; y++) {
        for (uint32_t x = 0; x < width; x++) {
            framebuffer[y * pitch + x] = color;
        }
    }
}

void screen_draw_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    for (uint32_t i = 0; i < h; i++) {
        for (uint32_t j = 0; j < w; j++) {
            screen_put_pixel(x + j, y + i, color);
        }
    }
}

void screen_putc(char c, uint32_t x, uint32_t y, uint32_t color) {
    if (c > 127) return;
    for (int i = 0; i < 8; i++) {
        uint8_t row = font8x8[(int)c][i];
        for (int j = 0; j < 8; j++) {
            if (row & (0x80 >> j)) {
                screen_put_pixel(x + j, y + i, color);
            }
        }
    }
}

void screen_print(const char *s, uint32_t x, uint32_t y, uint32_t color) {
    for (int i = 0; s[i]; i++) {
        screen_putc(s[i], x + i * 8, y, color);
    }
}

uint32_t screen_get_width(void) { return width; }
uint32_t screen_get_height(void) { return height; }
